

	public class Shape {
		 double width;
		 double radius;
		 double length;
		 double height;
		 
		 /**
		 * constructor without parameters
		 */
		 
		 Shape(){
			 radius=0;
			 width=0;
			 length=0;
			 height=0;
		 }
		 
		 /**
		 * @return the height
		 */
		public double getHeight() {
			return height;
		}

		/**
		 * @param height the height to set
		 */
		public void setHeight(double height) {
			this.height = height;
		}

		/**
		 * constructor with parameters
		 */

		Shape(double length,double width){
			 this.length=length;
			 this.width=width;
		 }
		 
		 
	
		
		
		
		
		
		 
		 
		

	}
